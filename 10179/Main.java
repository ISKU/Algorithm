import java.util.Scanner;

public class Main {
	public static void main(String... args) {
		Scanner input = new Scanner(System.in);
		int testCase = input.nextInt();

		while (testCase-- > 0) {
			double cost = input.nextDouble();
			System.out.printf("$%.2f\n", cost - (cost * 0.2));
		}
	}
}